package com.aaron_maze.maze.model;

public class Opener {
    int idOpener;
    int idRoom;
    int cost;

    public int getIdOpener() {
        return idOpener;
    }

    public void setIdOpener(int idOpener) {
        this.idOpener = idOpener;
    }

    public int getIdRoom() {
        return idRoom;
    }

    public void setIdRoom(int idRoom) {
        this.idRoom = idRoom;
    }

    public int getCost() {
        return cost;
    }

    public void setCost(int cost) {
        this.cost = cost;
    }
}
